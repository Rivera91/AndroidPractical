package model;

public class Cards {


    private String success;
    private String deck_id;
    private boolean shuffled;
    private int remaining;


    public String getSuccess() {
        return success;
    }

    public String getDeck_id() {
        return deck_id;
    }

    public boolean isShuffled() {
        return shuffled;
    }

    public int getRemaining() {
        return remaining;
    }



}
